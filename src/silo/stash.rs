//-- stash.rs -------------------------------------------------------------------------------------------------------------------------
use	crate::silo::{ Arr, Buff, IAccess, IArr, Stk, U32 };
use	crate::stalks::Atm;
use	std::sync::atomic::Ordering;

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Stash< T>
{
    _Buff: Buff< T>,
    _Sz: Atm< U32>,
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> Stash< T>
{

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	NewEmpty() -> Self
    {
        Self {
            _Buff: Buff::NewEmpty(),
            _Sz: Atm::New( U32( 0)),
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Create< Sz: Into< U32>, SzStk: Into< U32>, Dispenser>(
        sz: Sz,
        szStk: SzStk,
        dispenser: Dispenser,
    ) -> Self
    where
        Dispenser: Fn( U32) -> T,
    {
        Self {
            _Buff: Buff::Create( sz, dispenser),
            _Sz: Atm::New( szStk.into()),
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Size( &self) -> U32
    {
        self._Sz.Load( Ordering::Acquire)
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Clear( &self)
    {
        self._Sz.Store( U32( 0), Ordering::Release);
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Stk( &self) -> Stk< '_, '_, T>
    {
        Stk::Create( &self._Sz, self._Buff.Arr())
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	BuffOut( &mut self) -> Buff< T>
    {
        std::mem::replace( &mut self._Buff, Buff::NewEmpty())
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Pop( &self, val: &mut T) -> bool
    {
        self.Stk().Pop( val)
    }
    //-----------------------------------------------------------------------------------------------------------------------------

}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T: Copy> Stash< T>
{
    pub fn	New< Sz: Into< U32>, SzStk: Into< U32>>(  sz: Sz,
        szStk: SzStk,
        def: T,
    ) -> Self
    {
        Self {
            _Buff: Buff::Create( sz, |_| def),
            _Sz: Atm::New( szStk.into()),
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	DoGrow( &mut self, fillVal: T)
    {
        let  	newSz = if self._Buff.Size() == U32( 0) {
            U32( 1)
        } else {
            self._Buff.Size() * U32( 2)
        };
        self._Buff.Resize( newSz, |_| fillVal);
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Push( &mut self, val:  T)
    {
        while !self.Stk().Push( val) {
            if self.Size() == self._Buff.Size() {
                self.DoGrow( val);
            }
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Append( &mut self, arr: Arr< '_, T>)
    {
        let  	n = arr.Size();
        if n == U32( 0) {
            return;
        }
        let  	neededSz = self.Size() + n;
        if neededSz > self._Buff.Size() {
            let  	growTo = neededSz.max( self._Buff.Size() * U32( 2));
            self._Buff.Resize( growTo, |_| arr[0]);
        }
        let  	startSz = self.Size();
        unsafe {
            std::ptr::copy_nonoverlapping(
                arr.Ptr(),
                self._Buff._Ptr.cast::< T>().as_ptr().add( startSz.AsUsize()),
                n.AsUsize(),
            );
        }
        self._Sz.Set( startSz + n);
    }

}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T: Default> Stash< T>
{
    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	PushX( &mut self, val: &mut T)
    {
        while !self.Stk().PushX( val) {
            if self.Size() == self._Buff.Size() {
                let  	newSz = if self._Buff.Size() == U32( 0) {
                    U32( 1)
                } else {
                    self._Buff.Size() * U32( 2)
                };
                self._Buff.Resize( newSz, |_| T::default());
            }
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> Stash< T>
where
    T: From< usize> + Clone,
{
    //-----------------------------------------------------------------------------------------------------------------------------
    pub fn	DoIndexSetup( &self)
    {
        let  	arr = self._Buff.Arr();
        arr.DoIndexSetup();
        self._Sz.Store( arr.Size(), Ordering::Release);
    }
    //-----------------------------------------------------------------------------------------------------------------------------
}

