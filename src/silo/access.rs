//-- access.rs -----------------------------------------------------------------------------------------------------------------------
use	crate::silo::{ U32, USeg};

//---------------------------------------------------------------------------------------------------------------------------------

pub trait IAccess< 'a, T: 'a + ?Sized> {
    fn	Size( &self) -> U32;
    fn	At< K: Into< U32>>( &self, k: K) -> &'a T;

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	IsEmpty( &self) -> bool
    {
        self.Size() == U32( 0)
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	Last( &self) -> &'a T
    {
        return self.At( self.Size() -1);
    }
    
    //-----------------------------------------------------------------------------------------------------------------------------

    fn	USeg( &self) -> USeg
    {
        USeg::Create( U32( 0), self.Size())
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	Span< F>( &self, mut f: F) -> bool
    where
        F: FnMut( &T) -> bool,
    { 
        self.USeg().Span( |k| f( self.At( k)))
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	Traverse< F>( &self, mut f: F) 
    where
        F: FnMut( &T),
    { 
        self.USeg().Traverse( |k| f( self.At( k)))
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	Iter( self) -> AccessIter< 'a, T, Self>
    where
        Self: Sized,
    {
        AccessIter {
            _access: self,
            _index: U32( 0),
            _marker: std::marker::PhantomData,
        }
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

pub struct AccessIter< 'a, T: 'a + ?Sized, A>
{
    _access: A,
    _index: U32,
    _marker: std::marker::PhantomData< &'a T>,
}

impl< 'a, T: 'a + ?Sized, A: IAccess< 'a, T>> Iterator for AccessIter< 'a, T, A>
{
    type Item = &'a T;

    fn	next( &mut self) -> Option< Self::Item>
    {
        if self._index < self._access.Size() {
            let  	item = self._access.At( self._index);
            self._index = self._index + 1;
            Some( item)
        } else {
            None
        }
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< 'a, T> IAccess< 'a, T> for &'a [T]
{
    fn	Size( &self) -> U32
    {
        U32( self.len() as u32)
    }

    fn	At< K: Into< U32>>( &self, k: K) -> &'a T
    {
        let  	idx = k.into().AsUsize();
        unsafe { &*self.as_ptr().add( idx) }
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< 'a, T: 'a, const N: usize> IAccess< 'a, T> for &'a [T; N]
{
    fn	Size( &self) -> U32
    {
        U32( N as u32)
    }

    fn	At< K: Into< U32>>( &self, k: K) -> &'a T
    {
        let  	idx = k.into().AsUsize();
        unsafe { &*self.as_ptr().add( idx) }
    }
}

