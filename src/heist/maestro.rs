//-- maestro.rs ----------------------------------------------------------------------------------------------------------------------
use	crate::heist::atelier::Atelier;
use	crate::silo::uint::{ U16, U32 };

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Maestro< 'a>
{
    _Atelier: &'a Atelier,
    _MavenIndex: U32,
    _CurSuccId: U16,
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< 'a> Maestro< 'a>
{

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	New( atelier: &'a Atelier, mavenIdx: U32, curSuccId: U16) -> Self
    {
        Self {
            _Atelier: atelier,
            _MavenIndex: mavenIdx,
            _CurSuccId: curSuccId,
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Atelier( &self) -> &'a Atelier
    {
        self._Atelier
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	MavenIndex( &self) -> U32
    {
        self._MavenIndex
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	CurSuccId( &self) -> U16
    {
        self._CurSuccId
    }

    //-----------------------------------------------------------------------------------------------------------------------------

}

