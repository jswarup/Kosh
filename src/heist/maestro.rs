//-- maestro.rs ----------------------------------------------------------------------------------------------------------------------

use crate::heist::atelier::Atelier;
use crate::silo::uint::U32;

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Maestro< 'a>
{
    _Atelier: &'a Atelier,
    _MavenIndex: U32,
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< 'a> Maestro< 'a>
{

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	New( atelier: &'a Atelier, mavenIdx: U32) -> Self
    {
        Self
        {
            _Atelier: atelier,
            _MavenIndex: mavenIdx,
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	Atelier( &self) -> &'a Atelier
    {
        self._Atelier
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	MavenIndex( &self) -> U32
    {
        self._MavenIndex
    }

    //-----------------------------------------------------------------------------------------------------------------------------

}

