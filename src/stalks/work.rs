//-- work.rs -------------------------------------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------------------------------------------

pub trait IWork: Send + Sync {
    fn	DoWork( &mut self, worker: &dyn IWorker);
}
impl< F> IWork for F
where
    F: for< 'r> FnMut( &'r ( dyn IWorker + 'r)) + Send + Sync,
    {
    fn	DoWork( &mut self, worker: &dyn IWorker) 
    {
        self( worker);
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

pub type WorkFn< 'a> = dyn IWork + 'a;

//---------------------------------------------------------------------------------------------------------------------------------

pub trait IWorker {
    fn	PostJob( &self, job: Box< WorkFn< '_>>);
    fn	AsRaw( &self) -> *const () 
    {
        std::ptr::null()
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Worker;
impl Worker 
{
    pub fn	New() -> Self 
    {
        Self
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl IWorker for Worker 
{
    fn	PostJob( &self, mut job: Box< WorkFn< '_>>) 
    {
        job.DoWork( self);
    }
    fn	AsRaw( &self) -> *const () 
    {
        self as *const Self as *const ()
    }
}

