//-- work.rs -------------------------------------------------------------------------------------------------------------------------
use	crate::silo::arr::Arr;

//---------------------------------------------------------------------------------------------------------------------------------

pub trait IWorker
{
    fn	Post( &self, jobs: Arr<'_, Box<dyn FnMut(&dyn IWorker) + Send + Sync>>);
}

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Worker;

impl Worker
{
    pub fn	New() -> Self
    {
        Self
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl IWorker for Worker
{
    fn	Post( &self, mut jobs: Arr<'_, Box<dyn FnMut(&dyn IWorker) + Send + Sync>>)
    {
        for job in jobs.iter_mut() {
            job( self);
        }
    }
}

