//-- work.rs -------------------------------------------------------------------------------------------------------------------------
use	crate::silo::arr::Arr;

//---------------------------------------------------------------------------------------------------------------------------------

pub type WorkFn<'a> = dyn FnMut(&dyn IWorker) + Send + Sync + 'a;

pub trait IWorker
{
    fn	Post( &self, jobs: Arr<'_, Box<WorkFn<'_>>>);
}

//---------------------------------------------------------------------------------------------------------------------------------

pub struct Worker;

impl Worker
{
    pub fn	New() -> Self
    {
        Self
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl IWorker for Worker
{
    fn	Post( &self, mut jobs: Arr<'_, Box<WorkFn<'_>>>)
    {
        for job in jobs.iter_mut() {
            job( self);
        }
    }
}

