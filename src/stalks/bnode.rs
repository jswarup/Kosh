#[derive(Debug, PartialEq, Clone)]
pub enum BNode< T> {

    Leaf( T),
    Node {
        _Left: Box< BNode< T>>,
        _Right: Box< BNode< T>>,
    },
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> BNode< T>
{
    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	NewLeaf ( value: T) -> Self
    {
        BNode::Leaf( value)
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	New( _Left: BNode< T>, _Right: BNode< T>) -> Self
    {
        BNode::Node {
            _Left: Box::new( _Left),
            _Right: Box::new( _Right),
        }
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    pub fn	CountLeaves( &self) -> usize
    {
        match self {
            BNode::Leaf( _ ) => 1,
            BNode::Node { _Left, _Right } => {
                _Left.CountLeaves() + _Right.CountLeaves()
            }
        }
    }
}

