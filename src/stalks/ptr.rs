//-- ptr.rs -------------------------------------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------------------------------------------

pub struct RawPtr< T>( pub *mut T);

//---------------------------------------------------------------------------------------------------------------------------------

unsafe impl< T> Send for RawPtr< T>
{ }
unsafe impl< T> Sync for RawPtr< T>
{ }

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> Clone for RawPtr< T>
{
    fn	clone( &self) -> Self
    {
        *self
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> Copy for RawPtr< T>
{ }

//---------------------------------------------------------------------------------------------------------------------------------

impl< T> RawPtr< T>
{
    pub unsafe fn	as_mut( &self) -> &mut T
    {
        unsafe
        { &mut *self.0 }
    }
    pub unsafe fn	as_ref( &self) -> &T
    {
        unsafe
        { &*self.0 }
    }
}

