#[derive(Clone, Copy)]
pub enum XField< 'a>
{
    Str( &'a str),
    U64( u64),
    F64( f64),
    Bool( bool),
    Arr( &'a [XField< 'a>]),
    Null,
}

//---------------------------------------------------------------------------------------------------------------------------------
 
pub trait IXFlux
{
    fn	OStream( &mut self) -> &mut dyn std::fmt::Write;

    fn	Field( &mut self, field: XField); 

    fn	KeyField( &mut self, _key: &str, _value: XField< '_>) -> bool;
}

