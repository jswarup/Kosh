//-- jsonifc.rs -------------------------------------------------------------------------------------------------------------------------
use	crate::silo::{ IAccess };

//---------------------------------------------------------------------------------------------------------------------------------

pub enum JsonValue< 'a>
{
    Str( &'a str),
    U32( u32),
    F64( f64),
    Bool( bool),
    Null,
}

//---------------------------------------------------------------------------------------------------------------------------------

pub trait JsonListener
{
    fn	KeyValue( &mut self, _key: &str, _value: JsonValue< '_>) -> bool
    {
        true
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	OpenArray( &mut self, _key: &str) -> bool
    {
        true
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	CloseArray( &mut self) -> bool
    {
        true
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	OpenObject( &mut self, _key: &str) -> bool
    {
        true
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	CloseObject( &mut self) -> bool
    {
        true
    }

    //-----------------------------------------------------------------------------------------------------------------------------

    fn	KeyArray< 'a, T: 'a, A, F>( &mut self, key: &str, access: &A, mut f: F) -> bool
    where
        Self: Sized,
        A: IAccess< 'a, T>,
        F: FnMut( &T) -> JsonValue< '_>,
    {
        let mut res = self.OpenArray( key);
        let empty_key = "";
        access.Traverse( | val| {
            res = res && self.KeyValue( empty_key, f( val));
        });
        res && self.CloseArray()
    }
}

//---------------------------------------------------------------------------------------------------------------------------------

pub trait JSonIfc
{
    fn	ToJSon< L: JsonListener>( &self, key: &str, listener: &mut L) -> bool;
}

